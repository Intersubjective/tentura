import 'dart:convert';
import 'dart:io';

import 'package:pubspec_parse/pubspec_parse.dart';

import 'version_update.dart';

/// After `flutter build web`, scan [buildWebDir] and emit preload manifest + SW.
///
/// [version] is the single cache-busting build version (see
/// `resolveWebBuildVersion`); it is written into `index.html` + `manifest.json`
/// and reused for the preload manifest version and SW `CACHE_VERSION`, so all
/// four entry-point references always agree.
void generateWasmPreloadArtifacts({String buildWebDir = 'build/web', String? version}) {
  final webDir = Directory(buildWebDir);
  if (!webDir.existsSync()) {
    stdout.writeln('wasm_preload: skip — $buildWebDir missing');
    return;
  }

  final resolvedVersion = version ??
      Pubspec.parse(File('pubspec.yaml').readAsStringSync()).version.toString();

  // Align index.html bootstrap query + PWA manifest BEFORE scanning so the
  // emitted ASSET_PATHS / preload manifest match the SW CACHE_VERSION.
  versionUpdate(webDir: buildWebDir, version: resolvedVersion);

  final wasmFiles = <String>[];
  final preload = <String>[];

  String relPath(File f) {
    var rel = f.path.substring(webDir.path.length);
    if (!rel.startsWith('/')) rel = '/$rel';
    return rel.replaceAll(r'\', '/');
  }

  void consider(File f) {
    final name = f.path.split(Platform.pathSeparator).last;
    final rel = relPath(f);
    if (name.endsWith('.wasm')) {
      wasmFiles.add(rel);
    }
    if (name == 'flutter_bootstrap.js' ||
        name == 'main.dart.js' ||
        name == 'main.dart.mjs' ||
        (name.startsWith('main.dart') && name.endsWith('.part.js')) ||
        name == 'sqlite3.wasm' ||
        name == 'drift_worker.js') {
      preload.add(rel);
    }
    if (rel.contains('/assets/') &&
        (name.endsWith('.wasm') || name.endsWith('.js'))) {
      if (!preload.contains(rel)) preload.add(rel);
    }
  }

  for (final entity in webDir.listSync(recursive: true)) {
    if (entity is File) consider(entity);
  }

  // Bootstrap with version query when present in index.html
  final indexFile = File('${webDir.path}/index.html');
  var bootstrapPath = '/flutter_bootstrap.js';
  if (indexFile.existsSync()) {
    final indexHtml = indexFile.readAsStringSync();
    final match = RegExp(
      r'''flutter_bootstrap\.js\?v=([^"']+)''',
    ).firstMatch(indexHtml);
    if (match != null) {
      bootstrapPath = '/flutter_bootstrap.js?v=${match.group(1)}';
    }
  }
  if (!preload.contains(bootstrapPath)) {
    preload.insert(0, bootstrapPath);
  }

  String? mainWasm;
  for (final w in wasmFiles) {
    if (w.contains('main.dart.wasm') || w.endsWith('/main.dart.wasm')) {
      mainWasm = w;
      break;
    }
  }
  mainWasm ??= wasmFiles.isNotEmpty ? wasmFiles.first : null;
  if (mainWasm == null) {
    stdout.writeln('wasm_preload: no .wasm found in $buildWebDir');
    return;
  }
  if (!preload.contains(mainWasm)) preload.insert(0, mainWasm);

  final manifest = {
    'version': resolvedVersion,
    'mainWasm': mainWasm,
    'preload': preload.toSet().toList(),
    'prefetch': <String>[],
  };

  final manifestFile = File('${webDir.path}/wasm-preload-manifest.json');
  manifestFile.writeAsStringSync(
    const JsonEncoder.withIndent('  ').convert(manifest),
  );

  final assetPathsJson = jsonEncode(manifest['preload']);
  final sw =
      _serviceWorkerSource(version: resolvedVersion, assetPathsJson: assetPathsJson);
  File('${webDir.path}/tentura-app-cache-sw.js').writeAsStringSync(sw);

  stdout.writeln(
    'wasm_preload: wrote manifest (${preload.length} preload, main=$mainWasm)',
  );
}

String _serviceWorkerSource({
  required String version,
  required String assetPathsJson,
}) => '''
// Generated by wasm_preload_artifacts.dart — do not edit in build/web.
const CACHE_VERSION = '$version';
const CACHE_NAME = 'tentura-app-assets-' + CACHE_VERSION;
const ASSET_PATHS = $assetPathsJson;

const BYPASS_PREFIXES = [
  '/api/',
  '/shared/',
  '/firebase-messaging-sw.js',
  '/invite/',
];

function shouldHandle(url, request) {
  if (request.method !== 'GET') return false;
  if (url.origin !== self.location.origin) return false;
  for (const p of BYPASS_PREFIXES) {
    if (url.pathname.startsWith(p) || url.pathname === p) return false;
  }
  const pathKey = url.pathname + url.search;
  return ASSET_PATHS.some((path) => pathKey === path || url.pathname === path);
}

self.addEventListener('install', (event) => {
  self.skipWaiting();
});

self.addEventListener('activate', (event) => {
  event.waitUntil((async () => {
    const keys = await caches.keys();
    await Promise.all(
      keys
        .filter((k) => k.startsWith('tentura-app-assets-') && k !== CACHE_NAME)
        .map((k) => caches.delete(k)),
    );
    await self.clients.claim();
  })());
});

async function cacheFirst(request) {
  const cache = await caches.open(CACHE_NAME);
  const cached = await cache.match(request);
  if (cached) return cached;
  const response = await fetch(request);
  if (response.ok) {
    await cache.put(request, response.clone());
  }
  return response;
}

self.addEventListener('fetch', (event) => {
  const url = new URL(event.request.url);
  if (!shouldHandle(url, event.request)) return;
  event.respondWith(cacheFirst(event.request));
});
''';
